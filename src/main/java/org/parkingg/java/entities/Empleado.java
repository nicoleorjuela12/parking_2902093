public class Empleado {

    public String nombre;
    public String apellidos;
    public String codigo;


    //Constructores

    public Empleado() {
    }

    
    //Constructor 

    public Empleado(String nombre, String apellidos, String codigo) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.codigo = codigo;
    }



    



    


}
